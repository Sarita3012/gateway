package com.example.rest.webservices.restwebservices.filtering;

import com.fasterxml.jackson.annotation.JsonFilter;
import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

//@JsonIgnoreProperties(value={"name","state"})
//this is a better way to ignore fields than @JsonIgnore annotation with every field
@JsonFilter("FilterUser")
public class FilterUser {
    //@JsonIgnore
    private String name;
    private String city;
    //@JsonIgnore
    private String state;

    public FilterUser(String name, String city, String state) {
        this.name = name;
        this.city = city;
        this.state = state;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
